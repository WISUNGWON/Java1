
public class Variable {

	public static void main(String[] args) {
		int a = 1; // Number -> Integer
		System.out.println(a);
		
		double b = 1.1; // Real Number -> Double
		System.out.println(b);
		
		String c = "Hello world";
		System.out.println(c);
		
		/*Java에서는 변수가 어떤 데이터 타입을 가질 수 있는지 명확히 정의를 해야 한다.
		 *Why? 
		 *자바에서는, 변수를 선언할 때 지정한 데이터 타입과, 
		 *그 변수에 할당되는 데이터의 타입이 다를 경우 컴파일이 되지 않음 
		 *-> 할당하는 데이터의 타입이 내가 변수로 선언하고자 하는 데이터의 타입과 다름을 바로 알 수 있고, 
		 *이후에 그 변수를 사용할 때 데이터의 타입이 무엇인지 헷갈리지 않음. 입력은 까다롭지만 사용할 때 편리하다.
		 * 
		 */
		

	}

}
