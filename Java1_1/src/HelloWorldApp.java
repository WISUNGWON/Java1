public class HelloWorldApp {
	public static void main(String[] args) {
		System.out.println("Hello World!!!");
	}
	
}

/* 1. 자바프로그램의 기본
 * java 파일명은 class name과 같게 해야 한다.
 * main 메소드 안에 코드를 작성해 실행시킨다. 
 * Code(= Source, language) 
 * 자바라는 언어를 통해 컴퓨터에게 하고싶은 일을 시키는 것 = 코딩 .
 * 코딩의 결과 -> Application(App) or Program
 * */

/* 2. Java의 동작원리(하향식)
 * Java Source code.java (Compile)
 * Java Application.class (Run)
 * Java Virtual Machine (Run)
 * Computer
 * 1) computer에 java를 다운로드
 * 2) 다운로드를 통해 Java Virtual Machine설치
 * 3) Java Source code를 작성(사람이 이해할 수 있는 코드)
 * 4) 컴파일을 통해 사람이 이해할 수 있는 코드를 컴퓨터가 이해할 수 있도록 한 결과, Java Application.class을 생성
 * 5) 이클립스라는 컴파일러를 통해 Application을 읽고 Java Virtual Machine을 통해 컴퓨터로 실행 
 * */
